const repository = createLocalRepository("contentIntelligence.trends", () => ({
  externalTrends: seedExternalTrends(),
  internalSignals: seedInternalSignals(),
  topics: seedTopics(),
}));

export function getTrendState() {
  return repository.get();
}

export function getAllTrends() {
  const { externalTrends, internalSignals } = repository.get();
  return [...externalTrends, ...internalSignals];
}

export function getTrend(trendId) {
  return getAllTrends().find((t) => t.id === trendId) ?? null;
}

export function updateTrendStatus(trendId, status) {
  return repository.update((state) => ({
    ...state,
    externalTrends: state.externalTrends.map((t) => (t.id === trendId ? { ...t, status } : t)),
    internalSignals: state.internalSignals.map((t) => (t.id === trendId ? { ...t, status } : t)),
  }));
}

export function addTopicFromTrend(trend, overrides = {}) {
  return repository.update((state) => ({
    ...state,
    topics: [
      {
        id: nextMockId("topic"),
        trendTitle: trend.title,
        title: overrides.title ?? `围绕「${trend.title}」的选题`,
        storeId: overrides.storeId ?? trend.relevantStores[0],
        platform: overrides.platform ?? "douyin",
        category: trend.relevantCategories[0],
        product: trend.relevantProducts[0],
        trendType: trend.sourceType,
        riskLevel: trend.complianceRisk,
        opportunityScore: trend.opportunityScore,
        lifecycleHours: trend.estimatedLifecycleHours,
        status: "待评估",
        createdAt: new Date().toISOString(),
      },
      ...state.topics,
    ],
  }));
}

export function updateTopicStatus(topicId, status) {
  return repository.update((state) => ({
    ...state,
    topics: state.topics.map((t) => (t.id === topicId ? { ...t, status } : t)),
  }));
}

